package com.util;

public class ArithmeticOperation {
    public int plus(int target, int source) {
        return target + source;
    }

    public int minus(int target, int source) {
        return target - source;
    }

    public int multiply(int target, int source) {
        return target * source;
    }

    public int divide(int target, int source) {
        return target / source;
    }
}
